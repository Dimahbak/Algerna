const LANG_KEY = 'civismart_lang';
let currentLang = localStorage.getItem(LANG_KEY) || 'fr';

/**
 * Retourne la traduction pour une clé donnée
 */
function t(key, fallback, params) {
  const dict = I18N[currentLang] || I18N.fr;
  let val = dict[key] || (I18N.fr[key]) || fallback || key;
  if (params) {
    Object.keys(params).forEach(k => {
      val = val.replace('{' + k + '}', params[k]);
    });
  }
  return val;
}

/**
 * Applique les traductions sur tous les éléments [data-i18n]
 * et gère les attributs placeholder/title via data-i18n-placeholder / data-i18n-title
 */
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== key) {
      // For elements with child SVG/icons, only update text nodes
      if (el.querySelector('svg') || el.querySelector('i')) {
        // Find or create the text node
        let textNode = null;
        for (const node of el.childNodes) {
          if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
            textNode = node;
            break;
          }
        }
        if (textNode) {
          // Preserve leading/trailing whitespace pattern
          textNode.textContent = '\n        ' + val + '\n      ';
        } else {
          // Append text after SVG
          el.appendChild(document.createTextNode(' ' + val));
        }
      } else {
        el.textContent = val;
      }
    }
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });

  // Titles
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });

  // innerHTML (for elements with HTML content like alerts)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });
}

/**
 * Bascule la langue et applique les changements
 */
function setLang(lang) {
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);
  // Refresh Saksini si ouvert (pour suivre la langue active)
  if (typeof saksiniInit === 'function') {
    const w = document.getElementById('saksini-widget');
    if (w && !w.classList.contains('hidden')) saksiniInit();
  }

  // Direction RTL / LTR
  const html = document.documentElement;
  if (lang === 'ar') {
    html.setAttribute('dir', 'rtl');
    html.setAttribute('lang', 'ar');
    document.body.style.fontFamily = "'Noto Sans Arabic', 'Inter', system-ui, -apple-system, sans-serif";
  } else {
    html.setAttribute('dir', 'ltr');
    html.setAttribute('lang', 'fr');
    document.body.style.fontFamily = "'Inter', system-ui, -apple-system, sans-serif";
  }

  applyTranslations();

  // Update lang selector buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Invalidate Leaflet maps if visible
  setTimeout(() => {
    if (typeof carteMap !== 'undefined' && carteMap) carteMap.invalidateSize();
    if (typeof csMap !== 'undefined' && csMap) csMap.invalidateSize();
    if (typeof wsMap !== 'undefined' && wsMap) wsMap.invalidateSize();
  }, 100);
}

/**
 * Initialise la langue au chargement
 */
function initI18n() {
  setLang(currentLang);
}
